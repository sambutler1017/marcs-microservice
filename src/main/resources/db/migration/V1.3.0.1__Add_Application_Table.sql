CREATE TABLE application (
  id       INT          unsigned NOT NULL AUTO_INCREMENT,
  app_name VARCHAR(128)          NOT NULL,
  enabled  TINYINT(4)            NOT NULL DEFAULT 0,
  PRIMARY KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

